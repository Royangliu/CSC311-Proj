def _cell_float(value):
    if value is None:
        return 0.0
    s = str(value).strip()
    if s == "" or s.lower() == "nan":
        return 0.0
    try:
        return float(s)
    except ValueError:
        return 0.0


def _dist2(ax, ay, bx, by):
    dx = ax - bx
    dy = ay - by
    return dx * dx + dy * dy


def _majority_vote(neighbor_labels):
    counts = Counter(neighbor_labels)
    top = max(counts.values())
    tied = [lab for lab, c in counts.items() if c == top]
    return min(tied, key=lambda lab: _CLASS_ORDER.index(lab))


def predict(row):
    """One row: dict-like keyed by CSV headers (e.g. DictReader)."""
    x = _cell_float(row.get(_COLOURS))
    y = _cell_float(row.get(_OBJECTS))
    dists = []
    for i, (tx, ty, lab) in enumerate(_TRAIN):
        d2 = _dist2(x, y, tx, ty)
        dists.append((d2, i, lab))
    dists.sort(key=lambda t: (t[0], t[1]))
    k_nearest = [lab for _, _, lab in dists[:K]]
    return _majority_vote(k_nearest)


def predict_all(filename):
    with open(filename, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        rows = list(reader)
    return [predict(row) for row in rows]